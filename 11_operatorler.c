#include <stdio.h>

void main()
{

    system("cls");
    
    int a = 10, b=15;

    /*
        Karşılaşatırma Operatörleri (Comparison Operators)

        ==  : Eşit mi?

        >   : Büyük mü?
        >=  : Büyük ya da eşit mi?
        <   : Küçük mü?
        <=  : Küçük ya da eşit mi?
        !=  : Farklı mı?
    */

       printf("a == b: %d\n\n", (a == b));
       printf("a > b: %d\n\n", (a > b));
       printf("a >= b: %d\n\n", (a >= b));
       printf("a < b: %d\n\n", (a < b));
       printf("a <= b: %d\n\n", (a <= b));
       printf("a != b: %d\n\n", (a != b));


}